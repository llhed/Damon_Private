package com.anran.pojo;

public class T_SENSOR_IMM {

	
	private int id;
	private String sensor_Num;
	private String value;
	private String time;
	private String alert_Status;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSensor_Num() {
		return sensor_Num;
	}
	public void setSensor_Num(String sensor_Num) {
		this.sensor_Num = sensor_Num;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getAlert_Status() {
		return alert_Status;
	}
	public void setAlert_Status(String alert_Status) {
		this.alert_Status = alert_Status;
	}
	
	
	
}
